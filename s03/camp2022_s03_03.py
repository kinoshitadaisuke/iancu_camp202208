# speed of light in m/s
c = 2.9979 * 10**8

# frequency of radio wave in Hz
frequency = 880.65 * 10**6

# calculation of wavelength in m
wavelength = c / frequency

# printing result of calculation
print ("speed of light = %g m/s" % (c) )
print ("frequency of radio wave = %g Hz" % (frequency) )
print ("wavelength of radio wave = %g m" % (wavelength) )
